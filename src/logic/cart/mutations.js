import * as types from './mutation-types';
export const mutations = {
  [types.INIT_CART_NUMBER](state, o) {
    if (o > 0) {
      state.cart = o
    } else {
      state.cart = null
    }
  },
  [types.INIT_CART_LIST](state, o) {
    state.list = o
  },
  [types.INIT_MESSAGE_NUMBER](state, o) {
    if (o > 0) {
      state.message = o
    } else {
      state.message = null
    }
  }
}
